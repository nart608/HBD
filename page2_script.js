    function updateCodeDisplay() {
        codeDigits.forEach((digit, index) => {
            digit.textContent = currentCode[index] || '';
        });
    }

    // ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏Å‡∏≤‡∏£‡∏Å‡∏î‡∏õ‡∏∏‡πà‡∏°‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç
